            <h2>Riflessioni</h2>
            <br>
            <b>I personaggi sono caricature</b>, rappresentazioni del potere, della Chiesa, dei filosofi, di alcuni soggetti 
            che sono in grado di indirizzare e muovere la società in una determinata direzione che senza la morte è 
            inevitabilmente quella del caos. 
            <br><br>
            Sono tutti così, tranne il violoncellista che ci viene presentato nella seconda parte del libro: un uomo 
            solo, che si “destreggia” fra la vita in casa con il suo cane, il lavoro da musicista e le passeggiate la 
            domenica al parco; mi è sembrato un uomo già “morto” per certi versi, almeno fino a quando la sua strada si 
            incrocerà con quella della <b>morte</b>, onnipotente e sicura di sé e che Saramago ci pone addirittura come <b>superiore 
            all’uomo per caratteristiche positive</b>: ad esempio quando l’ecatombe che segue alla decisione della morte di 
            riprendere il suo lavoro viene paragonata ironicamente a “una potenza mortifera di cui si troverebbe comparazione 
            solo in certe deprecabili azioni umane” oppure quando la morte è delusa dalle conseguenze morali e sociali della 
            sua scomparsa dalla vita dell’uomo o quando si dimostra capace di ammettere di aver sbagliato.
            <br><br>
            Credo sia un libro che possa accontentare chi vuole giocare con l’immaginazione, chi vuole farsi una risata 
            facendo ironia sul nostro presente politico e sociale e chi vuole indagare le emozioni, le paure, i difetti, 
            le contraddizioni e tutto sommato la meraviglia dell’animo umano. 
