Le flashcards credo siano un ottimo strumento per aiutarsi con lo studio di qualsiasi materia, 
ma soprattutto nei casi in cui c’è da <b>ricordarsi cose a memoria come ad esempio parole, date e nomi</b>.<br> <br>
Sono schede, di solito accompagnate da illustrazioni,
con una parola da un lato e la relativa traduzione dall'altro. Le consiglio come metodo di <b>ripasso e consolidamento di nozioni</b> già viste:
una volta create o scaricate dovrete solo "giocare" a cercare di ricordare la traduzione della parola che, se non vi viene in mente, 
trovate subito sul retro della carta. <br><br>Potete anche creare dei raccoglitori a tema, 
giocare un po' fra una sessione di studio e l'altra o farci dei buchi
e portarvele dietro sugli anelli portachiavi.

<br><br>
 

Quando si tratta di creare flashcards belle credo che <a target="_blank" href="https://canva.com">Canva</a> 
sia lo strumento più valido al momento, un sito gratuito che è semplice da usare. 
Probabilmente anche Microsoft Word potrebbe prestarsi bene, ma il metodo già provato da me è questo che vado ad illustrarvi.  
