<h2 id="polyglot">Polyglot</h2>
Polyglotclub è un sito che ho usato nei primi anni dell’università e che non uso da un po’. <br>
Ho visto però che ancora esiste e che <b>conta milioni di utenti.</b><br>
Per adesso ho bisogno di qualcosa di più immediato come un’app per parlare con persone madrelingua e acquisire una certa 
fluidità nel parlato, ma questo sito è stato per me un’ottimo strumento di studio. 
<br><br> 
Polyglot funziona più come un <b>Forum</b> in cui si può fare amicizia e parlare, ma anche aprire thread con discussioni, 
domande e possibilità di correzione di testi, anche abbastanza lunghi. 


