            "Il giunco mormorante" è un favoloso libro di Nina Berberova del 1958 tradotto negli anni '90. Nina Berberova è stata 
            una poetessa e scrittrice che fa parte del panorama dei grandi della letteratura russa(per capirci 
            se la intendeva con Achmatova, Blok, Gorkij e quindi la storia, il vecchio secolo poetico russo) 
            e una delle voci degli <b>autori transfughi</b> dalla Russia della Rivoluzione.
            <br><br>
Introspettivamente perfetto, ha una struttura stupenda basata su una poesia di 
Tjutčev utilizzata come epigrafe e dalla quale prende il titolo. Non è pretenzioso, non ha mille pagine, ne ha poco più di 70, eppure vi giuro che quando lo leggerete si farà ricordare a lungo.
<br><br>
Berberova ha colto e ha descritto con lucidità e fermezza 
il concetto della <b>no man’s land</b>, perché e come difenderla.
<br><br>
<i>"Fin dai primi anni della mia giovinezza pensavo che ognuno 
di noi ha la propria no man’s land, in cui è totale padrone di sé stesso. […] Queste ore […] possono essere felicità, 
necessità, abitudine, ma sono comunque sempre indispensabili per raddrizzare la “linea generale” dell’esistenza"</i>.
<br><br>
I suoi pensieri si dispiegano in tre parti narrative e su tre città: 
Parigi, Stoccolma e Venezia e quella che può sembrare dall’inizio una storia d’amore fra due amanti, 
con un primo momento di separazione, poi incontro e un atto di coraggio finale, si è rivelata per me più una storia d’amore per sé stessi.
<br><br>
È un piccolo grande libro e uno dei miei preferiti perché fornisce il coraggio 
di dare corpo alla libertà e al mistero, di <b>difendere la nostra interiorità</b> e di volerci bene. 



