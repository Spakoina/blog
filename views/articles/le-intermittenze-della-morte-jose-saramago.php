            <h2>Trama</h2>
            <br>
            Questa prima frase del libro di José Saramago preannuncia un nuovo inizio, 
            “un paese intero in possesso dell’elisir dell’immortalità”.
            Il sogno di una <b>vita senza morte</b> concesso alla popolazione di una nazione senza nome. 
            Ma cosa accade alla società quando non si muore più? Come cambierà la Chiesa ora che 
            l’assunto principale della resurrezione e della vita dopo la morte non ha più motivo 
            di esistere? Cosa farà la politica? 
            Il cambiamento è grande e Saramago, con un’ironia pungente (il libro mi ha strappato 
            più di una risata) ci mostra come la società possa arrivare al caos. 
            <br><br>
            Se hai già letto qualcosa di Saramago sai che tutti <b>i suoi libri partono da un assurdo, 
            una improbabilità</b>. Penso non sia facile maneggiare l’inverosimile, ma le situazioni che
           Saramago progetta sono tali da sembrarci vere, quanto basta per immergerci nella storia e 
           scoprire con lui le (im)probabilità del reale.
           <br><br>

            <h2>Temi</h2>
            <br>
            La storia, soffermandosi nella prima parte su quello che accade alla società ormai sbarazzatasi 
            dell’incombenza della morte, offre vari pretesti per parlare di temi ancora attualissimi. 
            <br><br>
            Uno è quello delle <b>pensioni</b>: come pagare persone che saranno vecchie in eterno? 
            Considerando che queste aumenteranno nel corso del tempo, il quale non smette, come la morte, di fare il suo lavoro. 
            “Signore, se non riprenderemo a morire non abbiamo futuro”. 
            <br><br>
            A questo è collegata la capacità, tutta tipica dell’umanità, di <b>rimandare costantemente 
            la ricerca di soluzione ai problemi</b> (pensiamo alla gestione della pandemia del 2020 e a 
            come potevamo sicuramente essere più pronti ad eventualità del genere, tanto per citare 
            un esempio). “I problemi del futuro, che sia il futuro a risolverli” dice uno dei filosofi 
            che nel libro hanno il compito di riflettere sulle conseguenze di un futuro senza morte. 
            <br><br>
            Saramago racconta poi brevemente la storia di un contadino con una malattia incurabile, 
            sollevando un’altra questione ancora oggi cruciale. Senza l’intervento della morte, infatti, 
            alcuni personaggi del libro rimangono in un limbo, uno stato definito da Saramago di “morte ferma” e 
            molte persone scelgono di portare oltre il confine i propri cari malati, di là, dove ancora si muore.
            <br><br>
            L’autore ci regala una sorriso amaro ironizzando sulla linea sottile che separa il “dare la morte” e 
            “l’uccidere” facendo riflettere così sulle tematiche delicate e molto complesse dell’<b>eutanasia e del suicidio assistito</b>.
            <br><br>

            <h2>Un autore da leggere ad alta voce</h2>
            <br>
            Aprendo questo libro ci si trova di fronte a veri e propri <b>muri di testo</b>: l’autore non usa la 
            punteggiatura, tranne qualche rara virgola o punto che assumono la forma di una pausa breve nel 
            primo caso e di una pausa più lunga nel secondo, e una lettera maiuscola che permette di districarsi 
            fra i geniali dialoghi dei suoi personaggi senza nome.
            <br><br>
            Visto il modo di scrivere di Saramago, non ho potuto fare a meno di documentarmi sui motivi 
            delle sue scelte stilistiche. 
            <br><br>
            Ho ascoltato un po' di interviste di Saramago e in una ho trovato finalmente il racconto dell nascita 
            della sua peculiare modalità di scrittura. 
            Durante la raccolta di materiale per un libro in particolare l'autore <b>ha dovuto confrontarsi con la cultura 
            popolare, con un modo di tramandare le storie, i detti e gli aneddoti, sempre avvenuto oralmente</b> 
            fra i contadini dell’Alentejo, protagonisti del libro che stava scrivendo. Alla venticinquesima pagina 
            Saramago ha iniziato a scrivere non tenendo conto delle regole sintattiche, collegando i dialoghi diretti e 
            indiretti proprio come aveva ascoltato dai contadini. Senza rifletterci nemmeno era diventato uno di 
            loro, il modo di esprimersi era cambiato e a pensarci bene quando leggiamo una pagina scritta da Saramago ci sembra
            di ascoltare un discorso fatto a voce: le digressioni quasi infinite sono permesse, il ritmo è veloce, i dialoghi serrati.
            
            <br><br>
            Lo stile di Saramago è anche la naturale conseguenza di quello che vuole mostrarci nelle sue storie. Le regole 
            sintattiche si rompono facendoci provare un’esperienza di lettura diversa. La forma del testo è in questo 
            senso funzionale all’intento dell’autore: infatti <b>così può trasformarsi anche la realtà da noi sperimentata attraverso 
                la lettura. </b>
            <br><br>
            La forma possiede una sua funzionalità anche nella costruzione del libro. Saramago infatti incornicia la 
            storia di Le intermittenze della morte fra la stessa frase iniziale e finale del libro, creando una geniale 
            struttura circolare. Quando voltiamo l’ultima pagina del romanzo siamo certi che il ciclo descritto nell'opera si ripeterà.
            <br><br>
            
