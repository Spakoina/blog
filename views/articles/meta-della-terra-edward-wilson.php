Solo qualche settimana fa è venuto a mancare il biologo Edward O. Wilson, uno dei più 
grandi di sempre, che fino alla fine ha portato avanti le sue lotte a favore della biodiversità 
(termine da lui creato) e del benessere del nostro pianeta. 
Qualche mese fa ho letto questo suo libro che penso possa essere davvero illuminante e 
che possa mettere alla prova le proprie opinioni di ordine etico e morale.
<br>
<h2> Di cosa parla?</h2>
<br>
La biosfera è in pericolo e la proposta dello scrittore e biologo Edward O. Wilson è 
ambiziosa: affidare <b>metà della terra alla natura per poter salvare la vita</b>.
<br>
Leggendo questo libro mi sono accorta di quanto io sia poco capace di guardare al futuro
ponendomi obiettivi grandiosi, mentre W. ne parla con certezza: in un futuro prossimo 
metà della terra sarà zona protetta e i parchi e le riserve diventeranno centri di ricerca e istruzione.
<br>
Questa è la sua proposta per salvare l’equilibrio che sostiene la presenza di tutte 
le specie viventi ed è  l’unica considerazione possibile se vogliamo preservare anche 
la nostra esistenza sul pianeta Terra.
<br>
<h2> Wow</h2>
<br>
Fin dalle prime pagine ci si può sentire così piccoli nel comprendere quante forme di 
vita silenziosamente portano avanti la loro vita intorno a noi e quanto siano necessarie 
alla nostra stessa esistenza.
<br><br>
Wilson ci fa notare che attualmente abbiamo scoperto e documentato circa 2 milioni di 
organismi, ma probabilmente è stato stimato che esista ancora un totale di 8 milioni di 
creature e non c’è modo di sapere di conoscere le conseguenze della scomparsa di una specie 
dall’ecosistema o dell’inserimento di una nuova in un ambiente.<br><br>
Dobbiamo mappare e conoscere il più possibile di ciò che ci circonda, ma l’attuale 
ritmo di ricerca (circa 15.000 specie scoperte e descritte all’anno) è troppo basso 
e per questo bisogna fare ricerca e aumentare le aree protette.
<br><br>

Inoltre l'idea dei parchi nazionali è nato in America influenzata dai libri di Thoreau, 
Emerson, Muir e altri scrittori. Ma che figata è questa cosa che un giorno la salvezza 
della nostra specie o della vita sulla Terra potrebbe realizzarsi grazie all'ispirazione 
dalla letteratura?!
Piacevole da leggere: la passione dell'autore per la materia traspare tutta, in ogni 
singola riga del testo. 
Osservare ciò che mi circonda attraverso gli occhi di questo biologo così appassionato 
ha portato "un calore e una ricchezza nuovi" nella mia visione delle cose.

Un po’ più difficile da digerire nel lungo termine: spinge inevitabilmente a rivedere 
la proprie idee su questioni di morale ed etica.

