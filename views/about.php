<h2 class="text-spacing-2">SU PAPERGIRL BLOG</h2>
<br>
La storia inizia con un account Instagram creato 
all’inizio del 2021 <a class="text-spacing-2 article-title-link" href="https://www.instagram.com/papergirl.ninja/" target="_blank">@papergirl.ninja</a> .
<br>
Lo spazio concesso dai post di Instagram però è diventato presto troppo limitante: <br>
il social è fatto più che altro per condividere foto e quindi <b>per scrivere con più libertà</b> decido nel 2022 di crearmi un blog.
<br><br>
Con la pagina sull'Alfabeto ucraino il sito ha iniziato ad avere molte visite e ho deciso di creare una piattaforma che comprenda, oltre alla lettura,
pagine <b>sull'apprendimento</b> in generale, soprattutto dedicate alle lingue. 
<br><br>


