            È una giornata meravigliosa: fa caldo e il cielo è <b>limpido</b> (7). Davanti a noi c’è un giardino enorme e un altro piccolo palazzo alla fine della lunga piscina.  Non vedo l’ora di passeggiare lì, ma intanto continuo il giro…<br><br>

            C’è una sala della Musica con un pianoforte, delle sedie e dei quadri alle pareti. <br>
            Una sala da pranzo con una lunga tavolata e piatti di ceramica originali del settecento. <b>Non posso fare a meno di</b> (8) pensare a quante canzoni <b>avranno suonato</b> (9) qui e quante poesie avranno letto (10) vicino a quel pianoforte. 
            Immagino i ricchi pranzi e banchetti e l’atmosfera nella casa. <br><br>

            Cammino ancora. Mi fermo davanti allo specchio di una delle stanze. Quante persone <b>si saranno specchiate</b> (9) qui e quante storie <b>avranno sentito</b> (9) queste pareti! <br>
            Vedo un cartello vicino lo specchio e leggo che questa è la stanza dove c’è stato il primo incontro ufficiale di Mussolini e Hitler. Che strana sensazione! È la stanza più severa e buia del palazzo. <br><br>
            Ed ecco che arrivo alla Sala Da Ballo: la più bella di Villa Pisani. <br>
            La sala dove si ospitavano i balli e che tutta l’aristocrazia del 700-800 ha visto: i Re d’Italia, Carlo IV di Spagna, lo Zar Alessandro I, Ferdinando II di Borbone. <br><br>
            Guardo in alto e vedo un affresco meraviglioso: un capolavoro. Così come il lampadario in legno: quante candele ci <b>saranno volute</b> (9) per illuminare questo posto? Quanti balli <b>avrà ospitato</b> (9) questa sala? <br><br>
            Mi emoziono tantissimo al pensiero della gioia in questa stanza, della musica, dei balli e della bellezza dei vestiti dell’epoca. <br>

