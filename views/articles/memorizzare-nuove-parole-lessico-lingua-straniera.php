<h2> Perché usare queste schede? 3 buoni motivi</h2>
Vi chiederete (o almeno io da persona pigra me lo chiedo) come mai questo processo così strano: non basta tagliare un foglio in rettangoli e disegnarci sopra e scriverci? 
Sì anche, ma:<br><br>
<ul>
    <li>Costruendovi voi lo schema creerete le cards come volete voi.</li>
    <li>Mentre le elaborate con Canva state anche studiando.</li>
<li>Il lavoro finale sarà molto ordinato e sarà più facile da studiare.</li>
</ul>
Non vi preoccupate, come bonus alla fine dell’articolo trovate il file da scaricare se volete solo la griglia da ritagliare &#128513;.





Ma ora vediamo come fare nella pratica. Sicuramente ci sono mille modi per costruire qualcosa del genere, ma ecco come ho imparato a fare io col tempo.<br><br>
<h2>Creare il foglio di lavoro</h2>
Dopo essere entrati su Canva (se non siete registrati, per questo e per molti progetti, basta la versione gratuita,
dovrete solo inserire email e password) questa è la schermata che vi apparirà quando entrerete. <br>
Cliccate su <b>“Crea un progetto”</b> e poi in fondo al menù su <b>“Documento A4”</b> per aprire il foglio di lavoro.
