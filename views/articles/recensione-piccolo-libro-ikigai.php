        <h2>Qual è la vostra ragione di vita? </h2>




        Sì, lo so, non è proprio una domanda dalla risposta facilissima e infatti se penso al concetto del 
        “Trovare una ragione di vita” mi viene in mente un processo di ricerca doloroso e una conquista faticosa. <br><br>
        Ken Mogi, neuroscienziato giapponese, <a target="_blank" rel="sponsored" href="https://amzn.to/3MYu6jd">in questo libro</a> ci parla dell’Ikigai riuscendo a rendere leggero sia un argomento 
        come questo sia le mie riflessioni nel corso della lettura, rivelandosi un viaggio piacevole alla scoperta del Giappone e dei suoi valori. <br><br>
        L’ikigai è un concetto radicato nella cultura giapponese: rappresenta un atteggiamento mentale e racchiude 
        quelle che mi sembrano una certa sensibilità e una modalità di comportamento tipiche di questa popolazione. <br><br>
        Interessantissimo (forse solo per me che ho studiato lingue :p) l’accenno a Francis Galton, uno psicologo 
        inglese dell’800 secondo il quale alcuni tratti particolarmente rilevanti di un popolo finiscono per codificarsi nel 
        linguaggio di quella civiltà e più è presente la caratteristica, più è probabile che venga concettualizzata in un’unica parola. <br><br>
        Allora eccola qua:
        la parola <b>Ikigai</b>, composta da “Iki” vivere e “Gai” ragione. Chi coltiva l’ikigai, chi cerca una ragione 
        di vita, sente di poter aspirare a un’esistenza felice e attiva e per illustrare meglio questo concetto Mogi si affida a 5 pilastri. 
        In questo breve articolo mi piacerebbe parlare dei due che più mi hanno colpita. <br><br><br>

        <h2>Primo pilastro: cominciare in piccolo. </h2> 
        <em>“L’ikigai abita il regno delle piccole cose: l’aria mattutina, la tazza di caffè, il raggio di sole”.</em> <br><br>
        
        In Giappone si è soliti accompagnare il primo tè verde con qualcosa di dolce, proprio come qui in Italia.<br>
        Questa voglia di "dolcezza" al mattino nasconde qualcosa di scientifico alla base: infatti mangiare alimenti che ci piacciono al mattino fa rilasciare dopamina, il che rinforza l’azione (in questo caso di alzarsi) 
        che compiamo prima dell’arrivo della ricompensa. <br><br>
        
        Ho trovato di una semplicità e allo stesso tempo di una bellezza sconvolgente questa piccola cosa: 
        il potere di educare a un certo tipo di azione tramite una piccola ricompensa. 
        Un invito a saper essere genitori di se stessi 
        in un certo senso, senza bisogno di essere normativi o punitivi nei nostri confronti, anzi, per dirla 
        alla Jordan Peterson, <b>trattandoci come una persona che abbiamo la responsabilità di aiutare</b>, 
        (<a target="_blank" rel="sponsored" href="https://amzn.to/3ypY7EQ">Dettagli del libro su Amazon "12 regole per la vita. Un antidoto al caos"</a>). 
        Ken Mogi suggerisce di aiutare la ricerca del nostro Ikigai iniziando dal mattino, trovando un nostro piacere e 
        sfruttando la gioia che suscita in noi per farla lavorare a nostro favore. <br><br>
        Per scovare questi piaceri personali Ken Mogi ci aiuta ponendoci delle domande che anche io rivolgo a voi: 
        <b>"Quali sono le cose che per voi hanno più valore sentimentale? Quali sono le piccole cose che vi danno più piacere?"</b> 
        Mi piacerebbe leggere qualcosa nei commenti! <br><br>
        Si tratta di ottimi punti di partenza per riuscire a svelare il proprio ikigai e farne uno strumento per vivere 
        una vita più felice e appagata; scoprirlo spetta solo a noi, è una questione di <b>responsabilità</b> che non si può scaricare all’esterno. <br><br>

        <em>“Coerenza e obiettivi fanno brillare anche i frammenti più minuscoli di ikigai”.</em><br><br><br>
        <h2>Secondo pilastro: dimenticarsi di sè </h2>
        Uno dei principi chiave del buddhismo zen (il concetto più moderno è paragonabile alla Mindfulness) è la capacità 
        di dimenticarsi di sé e di <b>stare nel presente, qui e ora</b>. Ken Mogi, tramite l’osservazione della cultura giapponese, 
        lega questo concetto alla scoperta dei piaceri sensoriali. <br><br>
        <em>“Alleggerendoci del peso dell’io possiamo aprirci all’universo infinito dei piaceri sensoriali”</em>. <br><br>
        Tempo fa mi ero imbattuta in un video su Youtube della cerimonia del tè giapponese e mi aveva colpito l’attenzione 
        ai dettagli in ognuno dei gesti che accompagnano questo rituale. La cerimonia segue uno dei principi che sono alla base 
        della filosofia giapponese: l’Ichigo-ichie, la consapevolezza e il rispetto della natura effimera di qualsiasi incontro e quindi di quanto sia importante
        godersi e trarre il massimo da ogni momento. 
        Per trarre piacere dal qui e ora bisogna dimenticarsi di sé e lo si può fare ad esempio occupandosi dei piccoli 
        dettagli di qualche procedura, o entrando in uno stato di "flusso", stesso concetto incontrato nel libro di Goleman che avevo
        <a href="<?php echo $GLOBALS['base_complete_url']; ?>/article/intelligenza-emotiva-goleman" target="_blank">recensito</a> tempo fa. <br><br>
        Mi viene in mente una metafora legata all’occhio che ho letto in un libro di Frankl. Soltanto l’occhio malato riesce a 
        vedere qualcosa di sé stesso come l’occhio che ha dei difetti nel cristallino (pensiamo ad esempio alla cataratta). 
        <b>L’occhio sano vede bene solo se non vede se stesso</b>. <br><br>
        Mi è sembrata un po’ la stessa cosa con questo pilastro dell’Ikigai: bisogna arrivare a non vedere se stessi, per poter 
        rivolgere l’attenzione tutta all’esterno e al presente. <br><br><br>

        <h2>Conclusioni</h2>
        Non posso che consigliare vivamente di prendersi anche solo dieci minuti al giorno e leggere questo piccolo libro! Per riflettere su cosa sia per noi l'ikigai
        o semplicemente per lasciarsi guidare tra le varie curiosità dell'affascinante cultura giapponese.<br>
        Facendo un bilancio sulla mia ragione di vita, ancora non so con certezza quale sia e se ci sia. <br><br>
        Forse vale la pena agire sempre come se il nostro percorso avesse uno scopo per riuscire a sfruttare a pieno le 
        possibilità che la vita ci offre. Purtroppo non so nemmeno se la direzione che ho preso è giusta, anche perché molte cose 
        non dipendono da me e gli obiettivi cambiano continuamente nel corso della vita, ma grazie a questo libro ho capito 
        che se non si riesce a trovare l’Ikigai o non si hanno le idee chiare, proprio allora bisogna cercare, scavare nella direzione 
        di quello scopo per trovarlo, <b>anche solo partendo da una buona tazza di tè al mattino</b>.
