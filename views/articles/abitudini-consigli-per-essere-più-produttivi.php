                    più sciocco o all’apparenza inutile.<br> L’ispirazione ha a che fare con il cogliere un’idea fra le tante e lavorare su quella, più che con l'aspettare 
                    che arrivi l’idea geniale. </li> <br>
                <li><b>Inizia pensando già a tutto quello che sei riuscita o riuscito a portare a termine</b> e chiediti “Ora cosa sono capace di fare?” </li>
