            <h2>La prima condizione per scrivere un romanzo</h2>
            <br>Per scrivere secondo Saramago è necessario intanto <b>sedersi</b>.<br><br>
            Adoro l’ironia di questo scrittore (potete assaggiare qualcosa nella recensione del libro 
            <a  href="<?php echo $GLOBALS['base_complete_url']; ?>/article/le-intermittenze-della-morte-jose-saramago">
        “Le intermittenze della morte”</a>). <br><br><!-- comment -->
            Eppure sotto questa affermazione, se vogliamo anche un po’ banale, c’è un suggerimento da non sottovalutare e cioè quello di fare intanto un primo passo o,
            ancora meglio,<b> mettersi nelle condizioni migliori per iniziare </b> a svolgere quello che dobbiamo fare. <br><br>
            
            <h2>La creazione non è sofferenza.</h2><br>
            Parte da un’affermazione: <b>la pagina bianca è lì per essere scritta</b>. <br><br>
            L’idea dell’atto creativo è spesso associato alla sofferenza.
            Questo non riguarda solo la creatività, ma tutte quelle attività in cui bisogna portare 
            a termine un compito o arrivare a un risultato. Ma deve essere sempre così? 
            Non potrebbe essere più semplicemente una <b>questione di scelta?</b> <br><br><!-- comment -->
            Scegliere la <b>soddisfazione per il lavoro portato a termine</b> e non la sofferenza del 
            solo pensiero di una pagina bianca davanti a sé. <br><br> È lì bianca proprio per essere riempita. 
            E, pensa, a quante altre situazioni della vita può essere esteso questo ragionamento? <br><br>
            
            <h2>Non aspettare l’ispirazione</h2><br>
            L’intervistatore chiede a Saramago se la sua visione è simile a quella di Picasso. 
            Lui diceva infatti che l’ispirazione è 90% sudore e 10% ispirazione. <br><br>
            Saramago la pensa in un altro modo. <br><br>
            <i>“Alla fine cos’è l’ispirazione? Quando compare? Una persona sta lì seduta, 
            come per esempio io adesso, e all’improvviso arriva? No. Se tutto quello che ci passa 
            per la testa la chiamassimo ispirazione allora saremmo tutti ispirati." </i> <br><br>
            L’ispirazione ha a che fare con un processo mentale più sottile e lungimirante; un <b>saper intravedere, 
            cogliere e sfruttare un’idea</b>. L’artista secondo Saramago è in grado di capire se un’idea, in mezzo 
            alle tante idee che ci passano per la testa, può essere trasformata in qualcosa di più.<br><br><!-- comment -->
            Una volta notata la <b>afferra</b> e continua a lavorarci. <br><br>

            <h2>Come non sentirsi persi?</h2><br>
            Viene chiesto a Saramago se nel corso della scrittura di una delle sue opere o in generale si 
            sia mai sentito perso o in un momento di stallo. Qui arriva una risposta meravigliosa.<br><br> “No, fino ad ora no. […] A volte può succedere che mi domandi: 
            <i>“E agora o que é que faço? E agora depois disto, o que é que sou capaz de fazer?” </i><br><br>
            <i>“E ora che faccio? Ora, dopo questo, cosa sono capace di fare?”</i> <br>
           <br>
           Anche qui è tutta una questione di <b>approccio alle varie situazioni e capacità di farsi le domande migliori</b> per raggiungere i propri obiettivi.<br><br><!-- comment -->
            <h2>“Non avere fretta e non perdere tempo”</h2><br>
            Un consiglio che Saramago dava spesso agli scrittori più giovani. Potrebbe sembrare una contraddizione, ma non lo è. <br> <br>
            Non avere fretta, non pensare che tutto quello che si fa sia geniale e usa il tempo in modo che quello che fai possa darti un giorno 
            una soddisfazione più completa possibile. <br><br>
            <h2>Fare le cose per sé stessi. </h2><br>
            Nell’intervista parla delle Lusiadi di Camões e dice che quando quell’opera è stata scritta nel sedicesimo 
            secolo in Portogallo c’era una percentuale di analfabetismo del 96%.<br> <br>
            È un’opera rimasta lì ad aspettare che qualcuno la leggesse e venne scoperta a suo tempo diventando in seguito una delle 
            opere più importanti della letteratura portoghese.<br><br>
            Quell’opera grandiosa non esiste perché destinata al 4% della popolazione, <b>esiste perché Camões decise semplicemente di scriverla</b>. <br><br>

