                Se cerchi qualcosa <b>che ti faccia ridere in modo elegante e sottile</b>, una scrittura che lavori con l'assurdità di alcune situazioni, 
che sia un inno alla libertà in chiave umoristica, 'L'anno della lepre' potrebbe fare al caso tuo.<br><br>

Un giornalista e un fotografo, cinici e insoddisfatti delle loro vite, mentre sono in viaggio per lavoro investono una lepre. 
Il protagonista Vatanen, il giornalista, scende e vede che l'animale è ferito. 
Senza pensarci troppo decide di accudire la lepre ed entrare con lei nel bosco, senza però voltarsi più indietro.<br><br>

A quanto pare è un <b>libro cult</b> nei paesi nordici e quindi pensavo di essere strana io. 
Ne avevo parlato nelle storie su Instagram: inizialmente non riuscivo a farmi un'idea. 
Andando avanti nella lettura però, mentre la figura del protagonista si delineava meglio, 
sono riuscita ad incontrare quell'amore puro e incondizionato per un libro che cerco sempre nelle letture.
<br><br>

La storia è costellata di <b>atti di ribellione</b>, ogni capitolo è un piccolo episodio, dalle sorti perennemente e ineluttabilmente comiche.<br>
        Dialoghi asciuttissimi che accentuano la vena ironica e la <b>critica all'opprimente realtà del sud della Finlandia</b> e delle sue grandi città.<br>
Come soluzione: <b>la fuga</b>.<br>
        Non un'estraniazione, ma l'entrata in un mondo diverso, nella natura del nord del paese, che non avviene però senza qualche assurda conseguenza.<br><br>

Conosci Paasilinna? Hai qualche altro titolo finlandese da consigliarmi?


