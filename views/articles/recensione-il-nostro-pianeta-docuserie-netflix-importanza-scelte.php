<h2>Un piacere per gli occhi</h2>
<br>
Questa docuserie nasce da una collaborazione fra Netflix, WWF e Silverback Films, durata ben 4 anni e la cui voce narrante in inglese è di David Attenborough, 
ma la trovate anche tradotta in italiano con la voce del doppiatore Dario Penne.<br><br>
Innanzitutto come ho già accennato questa è una serie <b>da vedere, bella per gli occhi, maestosa</b>. <br>
Ci sono delle immagini che rimarranno per sempre impresse nella mia mente e che non avrei mai potuto 
nemmeno immaginare senza questa serie.<br><br>
Più volte sono rimasta letteralmente a bocca aperta nel vedere le particolarità fisiche di alcune 
specie (l’episodio 6 Mare aperto con gli animali bioluminescenti è spettacolare), opppure le loro modalità di 
caccia e corteggiamento (i piumaggi degli uccelli del paradiso sembrano essere stati creati dai costumisti 
migliori del mondo per accompagnare delle performance degne di Broadway) o, ancora, le distese infinite di animali 
che si riuniscono in un unico luogo per accoppiarsi o cercare i loro cuccioli appena nati.


