    <li>Puoi iniziare da <b>libri brevi</b>.</li>
    <li>Non iniziare col cellulare ti prego! <b>Il supporto dal quale si legge è molto importante</b> e il cellulare ti farebbe perdere la voglia di leggere. 
    Per qualcosa di più pratico ok il Kindle che è costruito appositamente per la lettura.</li>
 </ul>

