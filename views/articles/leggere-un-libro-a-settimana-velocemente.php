            La tua lista di lettura è ormai infinita? Ti piacerebbe leggere almeno 52 libri l’anno? Uno a settimana? <br><br>
So che esistono tecniche di lettura veloce e magari qualcuna è anche valida. In questo articolo vorrei soffermarmi su alcuni concetti, 
più generali se vogliamo, ma che mi hanno aiutato più di qualsiasi “tecnica” di lettura a leggere un buon numero di libri. <br><br>

<h2>Cos’è per te la lettura?</h2>
Il titolo del post è un po’ provocatorio. La vera domanda da farsi è: ha senso leggere così tanti libri in poco tempo? La risposta secondo me dovrebbe essere “Dipende”. <br><br>
Cos’è per te la lettura? <br><br>
Le persone sono fatte in mille modi diversi, le motivazioni che ti spingono a compiere determinate azioni sono altrettante e così i libri esistenti sul mercato. <b>Per quale motivo leggi?</b> <br><br>

<h2>Lettura per studio </h2>
La lettura fatta per studio è quella ad esempio per l’università, o quella che riguarda letture tecniche. Qui è importante puntare sulla quantità di parole lette oltre che sulla qualità. <br><br> 
<b>Con una lettura veloce si può organizzare lo studio, individuando velocemente le nozioni più rilevanti</b>. 
In questo caso è utile avere una prima fase di lettura veloce per estrapolare i concetti e le parole chiave e poi studiare come si vuole, con mappe, schemi, ecc. <br><br>

<h2>Per piacere!</h2>
<i>Sì Chia, per piacere! Quando arrivano i consigli su come leggere un libro a settimana?</i><br>
Un momento che ci arriviamo ;) <br><br>
Dicevo, le mie letture ultimamente sono quasi esclusivamente fatte per piacere. 
Anche se poi con la saggistica posso appassionarmi a un tema e iniziare a “studiarlo” con altre letture. 
