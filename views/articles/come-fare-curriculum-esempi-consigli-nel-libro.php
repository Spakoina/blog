<h2>Chi dovrebbe leggerlo?</h2>
<br>

<b>La prima parte</b> la consiglierei a chi non sa bene cosa fare e vorrebbe orientarsi 
nell’infinità di scelta che c’è al giorno d’oggi (penso a chi esce dalle superiori o a chi vuole cambiare lavoro, a qualsiasi età). <br><br>
<b>La parte centrale</b> è adatta a chi è in cerca di consigli pratici sulla scrittura del cv, delle mail, delle 
lettere di presentazione. Ci sono anche esempi di colloqui, le domande più comuni, le domande che possiamo fare noi in sede di colloquio e tanto altro. <br><br>
<b>Nella parte finale</b> Andreani ci spiega come valutare il lavoro una volta scelti, quali tempistiche darsi
per farlo, come imparare il più possibile dall’ambiente lavorativo.
 <br><br>
Una delle sezioni più belle è quella dedicata alla “crescita” nelle ultime pagine del libro, in cui ho 
trovato delle considerazioni preziose da una donna che ama il suo lavoro, si mette in gioco online con contenuti 
di qualità e innovativi. 
