La serie è formata da 8 episodi di circa 50 minuti l’uno, ognuno incentrato su un ambiente diverso: dai 
paradisi di ghiaccio dei mondi congelati, alle praterie americane, dai deserti e i suoi elefanti ai meandri 
bui degli oceani.<br><br>
Gli episodi mostrano non solo i residenti di questi habitat, ma anche <b>l’interdipendenza</b> 
che esiste fra questi animali e come la mancanza di una sola delle specie che lì vi abitano o 
alcuni cambiamenti del clima dovuti al riscaldamento globale possano a catena rompere tutti gli equilibri che permettono la vita. <br><br>
Metà dell’ossigeno che respiriamo, ad esempio, è prodotto dal bosco marino del fitoplancton che, proprio come i nostri boschi sulla terra,
creano particelle che aiutano l’umidità a formare le nuvole, regolando così il clima e trasportando acqua fresca in tutto il mondo.

