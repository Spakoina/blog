<h2>Due consigli preziosi</h2><br>
Uno dei motivi per cui sono contenta di aver letto questo libro è la scoperta di un’idea che si trova verso la fine del libro: 
nei momenti di caos emozionale è bene portare l’attenzione alla pancia perché <b>tenersi a livello dell’intelletto è pericoloso</b>. 
Più volte l’autore aiuta a comprendere più a fondo i concetti tramite metafore e quella riservata a questa idea l’ho trovata 
di particolare efficacia, tanto che a distanza di mesi dalla lettura del libro continua a tornarmi in mente ed è stata di grande 
aiuto in alcune occasioni. <br><br>
Hahn ci dice di osservare la cima di un albero durante un temporale. Essa è vulnerabile e instabile, mentre se guardiamo il 
tronco si ha un’impressione diversa: l’albero è solido e tranquillo e resiste.<br><br>
<b>La respirazione e l’attenzione verso il basso, a livello della pancia, aiuta a essere in contatto col presente</b> e a notare come 
l’emozione sia solo un’emozione: lei arriva, sta con noi un po’ e va via. Alla fine la tempesta passa. <br><br>
Alto consiglio prezioso riguarda una <b>tecnica di rilassamento profondo</b> che si trova in appendice. Anche qui ho trovato un po’ 
di esagerazione nella modalità presentata per svolgere l’esercizio: portare amore verso le parti del corpo non so quanto sia 
possibile, ma ho notato che <b>prendere consapevolezza di ogni singola parte del corpo</b>, prima di dormire ad esempio, cercare di 
sentire la tensione che si allenta in ogni piccolo muscolo del volto, rilassare le spalle e lasciar sprofondare il corpo per 
rilasciare la tensione mi aiuti ad addormentarmi molto prima e in generale a rilassarmi più di qualsiasi cosa io abbia provato già. 
Quasi più del leggere prima di andare a dormire! <br><br>

<h2>Conclusioni finali</h2><br>
Non sono né particolarmente entusiasta né particolarmente frustrata dalla lettura di Spegni il fuoco della rabbia 
di Thich Nhat Hanh: il libro è l’esposizione di una filosofia e si rivela a tratti un po’ stucchevole. <br><br>
Lo sconsiglio se leggete spesso saggi scientifici o se siete allergici ai discorsi apodittici. <br>
Io sono molto contenta di essere venuta a conoscenza di alcuni concetti e credo che alcuni passaggi possano essere 
fonte di riflessione, nonostante le critiche che mi sono sentita di rivolgere a qualche parte del libro che riguarda 
più la forma che il contenuto in sé. 

            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            



        
        
