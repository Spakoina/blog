<h2 id="gimm">Google Immagini </h2>
Questo è un trucco che ho sempre pensato fosse conosciuto da tutti e una cosa naturale da fare per un risultato immediato. <br>
Come ho avuto modo di vedere dai miei compagni di università, e ancora adesso quando parlo con chi studia lingue, <b>è una pratica che crea sempre tanto stupore</b>. 
Di cosa sto parlando? Di cercare su Google Immagini la parola che non si conosce!<br><br> 
Non sai che significa quella parola che hai appena sentito nella serie in lingua che stai guardando, 
ma non hai voglia di cercare sul dizionario e leggere. Vai su Google immagini, digita la parola et voilà, <b>vedrai con i tuoi occhi il significato</b>.<br>
A volte è bello avere una risposta immediata ai propri dubbi e andare avanti con il film, col libro, con quello che si sta ascoltando,
