"La vita involontaria" di Brianna Carafa mi è sembrato un piccolo universo che, proprio come quello in cui viviamo, è formato per la maggior parte da materia che non riusciamo a vedere.

È un libro piccolo, ma denso nella scrittura, particolarmente intenso, ma mai pesante.

Pintus è un orfano nato in una città immaginaria della Germania che si trasferisce a Vallona per studiare filosofia.

Con una disgregazione familiare alle spalle e nessuna prospettiva davanti a sé Pintus vive la sua vita "involontaria", da inetto, <b>trascinato dagli eventi</b>.
<br><br>

In questo romanzo di formazione la "materia oscura" è rappresentata da tutte le cose non dette, le assenze e gli atti mancati nella vita del protagonista.

Grazie alle parole di Carafa mi sono chiesta anche io (insieme a Pintus) quale effetto abbiano avuto sulla mia vita la casualità, le cose non dette, gli atti mancati e quale sia il confine fra una vita "passiva" e una vita vissuta semplicemente <b>assecondando gli eventi</b>.
E voi? Assecondate o subite passivamente la vita?

