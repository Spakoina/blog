Nei primi sei mesi del 2022 vediamo in crescita rispetto all’anno precedente fumetti, narrativa straniera, guide turistiche (+100%!), romanzi d’amore e chick lit. <br><br>
In tutti questi casi ha senso leggere il più velocemente possibile? <b>Credo che questo bidogno vada in direzione opposta alla voglia di assorbire nuove idee o godersi la lettura, proprio perché in questo modo non c’è il tempo sufficiente per farlo</b>. <br><br>
Inoltre è impossibile trovare così tanti testi di qualità da leggere in un anno. <br><br>


<h2>L’unico vero segreto per riuscire a leggere velocemente è…rullo di tamburi</h2>
<b>Leggere!</b>
Sì, solo leggendo si riesce a leggere sempre di più, senza fatica <b>(È forse questo che si intende per velocità? Più che macinare parole su parole con delle tecniche?)</b> e si impara a <b>costruire un’abitudine alla lettura</b>. 
<ul>
  <li>Se nell’ultimo anno hai letto poco o nulla non ha alcun senso andare alla ricerca di tecniche per velocizzare la lettura.</li>
  <li>Leggendo imparerai a <b>gestire la pagina</b> e comprendere delle frasi addirittura istantaneamente, solo con uno sguardo.</li>
  <li>Avrai <b>un ritmo diverso</b>, pur comprendendo sempre quello che stai leggendo.</li>
  <li>Pian piano <b>comprenderai il ritmo dell’autore</b> ed ecco che la lettura diventerà fluida.</li>
</ul> 
 
<br>

<h2>Come si costruisce l’abitudine della lettura?</h2>
<ul>
    <li>Intanto leggendo libri che ti piacciono! <b>Non leggo mai un libro che so che non finirò</b>: se non ti sta emozionando, 
        se non ti sta dando alcuna informazione utile, se non sta stimolando nessun ragionamento, meglio lasciarlo lì. <br> 
        La vita è troppo bella per incaponirti su qualcosa che non mi sta dando niente e poi puoi sempre riprenderlo più in là e riprovare.</li>
