            <ul>

                <li><b>Inizia la tua attività da un semplice passo</b>.
                Spesso siamo poco produttivi perché non sappiamo da dove iniziare e sprechiamo tempo a controllare la posta elettronica, 
                a fare un giro sui social, a leggere le notizie, ecc. <br>Per evitare tutto questo iniziamo da un piccolo primo passo. 
                Non credo che Saramago e Ken Mogi si siano frequentati molto, ma è qualcosa di cui ti avevo parlato 
