                Questo piccolo passo è fondamentale. Più di una volta mi è stato di aiuto anche in attività semplici che continuavo a rimandare come 
                la pulizia della casa: basta iniziare a pulire una mensola e, senza accorgertene, ti ritrovi ad aver pulito praticamente tutta la stanza.</li><br>
                
                <li><b>Inizia un compito pensando alla soddisfazione</b> del lavoro portato a termine più che alla sofferenza del momento.</li> 
                Ci vuole grande lucidità probabilmente e tanta forza d’animo. 
                <br><br>
                
                <li><b>Inizia mettendo per iscritto tutto quello che ti passa per la testa</b> in una specie di brainstorming di idee. 
