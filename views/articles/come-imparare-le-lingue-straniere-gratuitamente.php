<h2 id="reverso">Reverso Context</h2>
Questo è uno strumento utilissimo nel momento in cui si apprende una nuova <b>parola</b>.<br> Lo trovo indispensabile per scovare quelli che in linguistica sono chiamati
"Falsi amici", cioè quelle parole che assomigliano 
a un vocabolo della nostra lingua, ma che nella lingua che stiamo imparando hanno tutt’altro significato.
<b>Reverso Context permette di vedere quella parola all'interno di diversi contesti</b> e capire la sua funzione nella lingua. <br><br>

Per iniziare a tradurre basta che tu vada sul  <a href="https://context.reverso.net/traduzione/sito" target="_blank"><b>Reverso Context</b><i class="fas fa-external-link-alt"></i></a> e digiti nella barra di ricerca la parola o anche l’espressione che vuoi cercare, scegliendo poi la lingua che ti interessa.
Il sito ti darà una serie di combinazioni di traduzioni tratte di solito da contesti di vita quotidiana. <br>
Reverso Context ha una funzione di <b>lettura della parola o della frase</b> in lingua originale, anche questa molto comoda. <br><br>
Altro punto di forza è la <b>proposta di sinonimi</b> per cogliere ancora meglio il significato della parola o dell’espressione che stiamo cercando.
Esiste anche l’app, ma non l’ho mai usata quindi non saprei dirvi se sia buona o meno.
