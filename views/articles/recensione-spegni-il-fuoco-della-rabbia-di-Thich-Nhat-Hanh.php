Non leggo solitamente libri che riguardano il buddhismo, credi religiosi in generale o che abbiano 
a che fare con la spiritualità, ma proprio per il motivo di cui sopra ho provato comunque a dare 
un’occhiata a <i>Spegni il fuoco della rabbia</i> di Thich Nhat Hanh. <br><br>

<h2>Cosa non mi è piaciuto?</h2><br>
Ci sono cose che non ho particolarmente apprezzato: la prima parte soprattutto, 
in cui si trovano una serie di consigli che ho trovato molto banali, pieni di cliché 
e semplificazioni, con nulla di scientifico alla base, ma guidati dai principi spirituali del Buddhismo e della filosofia zen. <br>
Ci sono molte ripetizioni di frasi e concetti che affaticano un po’ la lettura, 
ma che si possono tollerare, visto l’intento educativo dell’opera. <br><br>
Tuttavia, siccome <b>credo di aver trovato degli spunti utili in questo libro</b>, 
ho provato a riassumere ciò per cui vale la pena secondo me leggerlo. <br><br>

<h2>Avere cura delle proprie emozioni</h2><br>
L’intento dell’autore, un monaco buddhista, è di insegnarci ad avere a che fare con le emozioni negative, 
le quali non sono altro che una modalità del nostro corpo di farci sapere che qualcosa non va e che 
c’è qualcosa di cui dobbiamo prenderci cura. <br><br>
<i>“Se ti viene un problema allo stomaco o ai polmoni non li butti via. Lo stesso vale per la rabbia: va accettata 
perché sai che puoi prenderti cura di lei”</i>. <br><br>
Penso sia bella questa attenzione e cura verso quello che ci può essere di brutto nei nostri pensieri e nelle 
nostre <b>emozioni: non sono da ignorare, da combattere o reprimere, non siamo su un campo di battaglia</b>, vanno accettate 
tutte, anche quelle negative, ma come? <br><br>

<h2>La presenza mentale</h2><br>
L’autore fornisce alcuni strumenti, come una tecnica che riguarda il respiro o un particolare tipo 
di camminata che possono aiutarci ad essere in presenza mentale, uno stato di consapevolezza che può 
aiutare a trasformare le emozioni negative, proprio come si fa con il compost (per utilizzare una 
delle metafore di Thich Nhat Hanh). <br><br>
La presenza mentale impedisce anche di ricorrere ad espedienti di controllo delle nostre emozioni o 
di sfogo dannoso. Siamo noi i responsabili della rabbia, ma crediamo ingenuamente che soffriremo meno 
se puniamo anche gli altri. L’autore suggerisce invece che in quei momenti sarebbe meglio non dire e non fare nulla, 
perché <b>la punizione dell’altro alla fine è punizione di sé stessi</b>. <br><br>
Sempre a proposito della presenza mentale Hanh racconta di un incontro con una donna che aveva saputo della 
passione del maestro per l’orto: <br><br>
<i>“Caro maestro tu scrivi poesie così belle. Passi anche molto tempo a coltivare lattuga e ortaggi. Perché non 
passi più tempo a scrivere poesie? Quella donna pensava in maniera pragmatica e mi suggeriva di non sprecare 
il mio tempo lavorando nell’orto, ma di usarlo per scrivere poesie”</i>. <br><br>
Lui le rispose:<i>”Mia cara amica, se non coltivassi la lattuga non potrei scrivere le poesie che scrivo”</i>. <br><br>
Nella nostra società orientata verso il risultato, gli obiettivi, i numeri, pensiamo spesso che il tempo sia 
denaro e che tutto debba essere veloce e remunerativo (inteso nel senso economico e di risultati ottenuti). <br><br>
Questa storia mi ha fatto riflettere sul bisogno di vivere a al meglio ogni momento della vita, anche quelli 
che lì per lì sembrano inutili. Il tempo utilizzato per stare bene non è tempo sprecato e, paradossalmente, 
rende più efficace tutto il tempo vissuto nella nostra vita.
<b>Se non vivi a fondo ogni momento della tua vita non puoi scrivere, non puoi produrre nulla di valido da offrire agli altri</b>. <br><br>


