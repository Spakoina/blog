<h2>La responsabilità di sé stessi</h2><br>
Grande risalto nel libro è dato all’importanza della <b>scelta del proprio percorso lavorativo</b>:
è una scelta fatta per se stessi e nella quale è bene che nessuno influisca (genitori in primis). <br><br>
Altra cosa che l’autrice ci fa notare è come <b>nessuna scelta sia uno sbaglio di per sé</b>, ma qualsiasi cosa 
    faccia parte del bagaglio delle nostre esperienze. 
Quante possibilità in più apre affrontare la ricerca del lavoro con la consapevolezza che ogni piccola 
esperienza può portarti poi ad un lavoro che ti piace? <br><br>
L’autrice invita a ritagliarti un po’ di tempo con un esercizio  <b>per capire quali siano i tuoi valori, 
    gli ideali che ti guidano verso uno scopo e cosa ti renda soddisfatto</b>.<br><br>

<h2>Un cambio di prospettiva</h2><br>
Altra cosa che mi ha insegnato questo libro è il sapersi mettere nei panni dell’altra persona. <br>Ad esempio 
un <b>cv brutto non predispone alla lettura, oltre che denotare scarsa cura</b>: sapere che il proprio curriculum 
rischia addirittura di non essere letto può essere antipatico da accettare, ma mettendoti nei panni di un 
recruiter, che riceve migliaia di curricula, questa verità comincia a diventare quantomeno comprensibile. <br><br>
Inoltre, prima di leggere questo libro pensavo di avere un curriculum fatto abbastanza bene. Ho scoperto 
invece che molti elementi non erano per nulla chiari, considerando che l’avrebbe letto una persona che di 
me non sa nulla. Tante altre parti erano proprio mancanti, come la <b>sezione sugli Hobby, che ho scoperto essere importantissima</b>. <br><br>
Ho imparato a considerare il <b>curriculum uno strumento</b> in cui non è necessario scrivere tutto nei minimi 
dettagli, ma <b>che ti permette di presentarti e valorizzarti al meglio</b>: 
è un’occasione per fare il punto su chi sei e cosa vuoi diventare. <br><br>
Conoscere alcune dinamiche interne aiuta ad affrontare meglio il processo di ricerca del lavoro che non 
rimane solo una fase antipatica, in cui <b>tante volte si viene scartati e nella maggior parte non si riceve 
nemmeno una risposta, gettandoci nello sconforto</b>. 
Diventa anzi un’occasione per auto valutarsi, per impostare un orientamento dei propri obiettivi, e 
<b>rappresenta un momento da affrontare a testa alta, con caparbietà</b>, sapendo che il non ricevere feedback nella 
maggior parte dei casi è assolutamente normale e che questo non rappresenta un fallimento, ma un’indicazione 
del fatto che ci stiamo muovendo e che siamo un passo sempre più vicini al prossimo lavoro. <br><br>

