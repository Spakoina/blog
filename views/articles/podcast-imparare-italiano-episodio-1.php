            La famiglia Pisani, infatti, era una delle più importanti famiglie Venete, ma non è l’unica famiglia ad aver vissuto in queste stanze.<br>
            Cammino e una delle prime sale che visito è una camera da letto tutta color oro. <br>
            Nel 1807 la villa è stata comprata da Napoleone Bonaparte e questa era la sua camera! C’è un letto a baldacchino enorme, mobili preziosi e intarsiati. <b>Addirittura</b> (5) un bagno privato con una vasca scolpita nel marmo e mobili di legno stupendi. <br><br>
            Ancora un corridoio. Una delle guide ci apre la finestra per <b>farci vedere</b> (6) il paesaggio all’esterno e wow! <br>

