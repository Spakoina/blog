<h2 id="tandem">Tandem</h2>

    Iniziamo con la mia preferita:Tandem.<br><br>
È un’app gratuita con una community di milioni di utenti provenienti <b>da tutto il mondo</b>. 
Consente di mettersi in contatto con persone che vogliono imparare la tua lingua. <br><br>
Per iniziare a usare Tandem bisogna <b>scaricarla</b> se hai iOS/iPadOS su <a href="https://apps.apple.com/it/app/tandem-scambi-linguistici/id959001619" target="_blank"> Google Play Store<i class="fas fa-external-link-alt"></i></a> oppure per gli altri dispositivi da 
<a href="https://play.google.com/store/search?q=tandem&c=apps&hl=it"target="_blank"> Android<i class="fas fa-external-link-alt"></i></a>.
 <br>
Una volta dentro l’app <b>registrati</b> seguendo la procedura guidata. Bastano pochissimi dati.<br>
Cliccando su <b>“Community”</b> in basso a sinistra potrai scorrere tra i vari profili delle persone e metterti in contatto con qualcuno se nel 
frattempo non l’hanno già fatto con te. 
<br><br> 
L’impostazione è simile a Whatsapp con chat e possibilità di <b>inviare audio e foto</b>. 
Il <b>punto di forza, secondo me, è la possibilità di correggere i testi</b> della persona con cui si sta chattando, in modo facile 
e con una grafica davvero ben fatta, rendendola un’app potentissima per il potenziamento della lingua scritta.<br><br> 
