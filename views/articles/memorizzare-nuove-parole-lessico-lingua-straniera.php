Ora passiamo alle misure.<br><!-- comment -->
Selezionando un angolo della figura e muovendo il cursore stabilite la misura del rettangolo, 
più o meno dovrà essere <b>s: 5 e h:8</b> per ottimizzare al meglio lo spazio del foglio A4. <br>
Fate copia e incolla (ctrl+c e ctrl+v) e createvi altri 3 rettangoli che andrete a posizionare vicini. <br>
Poi copia e incolla di quei 4 e create tre righe da mettere una sotto l’altra, così:

