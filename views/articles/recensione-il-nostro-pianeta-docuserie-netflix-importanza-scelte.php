<h2>Cosa possiamo fare noi?</h2>
<br>
Rispetto ai documentari del passato secondo me la docuserie "Il nostro pianeta" ha qualcosa in più: fa riflettere sullo spettacolo della natura, 
ma anche su cosa minaccia la loro esistenza sulla terra, <b>proponendo soluzioni</b> o mostrando il problema sotto altri punti di vista.<br><br> 
È vero che il pesce sta per finire, un terzo delle riserve mondiali di pesce è già finito, ma esiste la <b>pesca sostenibile</b>.<br>
Un decimo del pesce che mangiamo è composto da acciughe, per evitare che non rimanga niente si deve 
tener conto dell’importanza delle <b>zone protette</b>. 
A proposito dell'ambiziosità dell'immaginare un mondo pieno 
di zone protette Edward O.WIlson ci ha visto lungo e ne ho parlato nell'articolo che si chiama <a href="https://www.papergirlblog.com/article/meta-della-terra-edward-wilson" target="_blank">Immaginare il futuro con "Metà della Terra".</a> <br>
L’agricoltura distrugge alcuni habitat, ma l’agricoltura tradizionale fatta in Ungheria lascia campi 
con la loro enorme ricchezza naturale.<br><br>
Insomma le risorse sanno rigenerarsi, la natura è ricca: non c'è bisogno di abusare della sua abbondanza,
ma di capire come sfruttarla intelligentemente e in modo sostenibile, cioè in modo che ce ne sia sempre per tutti e per le generazioni future.
Imparare i meccanismi che regolano la vita intorno a noi penso sia fondamentale per imparare anche a prendersene cura.
<br><br>
Alla fine di ogni episodio inoltre c’è l’invito 
a visitare il sito <a href="https://wwf.panda.org/projects/our_planet_netflix_wwf_nature_documentary/what_can_i_do/?#whatibuy"target="_blank">WWF collegato a Our Planet</a>
per approfondire su cosa sia possibile fare nel nostro piccolo perché <b>non siamo i soli ad aver bisogno di acqua dolce, 
dei mari, delle foreste e dei deserti, ma siamo i soli ad avere potere di scelta</b>: 
scegliere innanzitutto di conservare questa ricchezza, in modo sostenibile, e decidere come distribuirla.
