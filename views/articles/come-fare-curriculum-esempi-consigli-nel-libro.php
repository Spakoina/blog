                “Lavorare alla grande” è un libro che tratta di <b>orientamento al mondo professionale</b>.<br>
L’autrice si chiama Fabiana Andreani ed è nota su Instagram col nome di <a href="https://www.instagram.com/fabianamanager/" target="_blank">@fabianamanager</a>. 
Quando ho saputo dell’uscita del suo libro l’ho comprato senza pensarci perché ero in piena fase di ricerca del lavoro 
e quello che ho trovato ha superato le mie aspettative, già molto alte, vista la bravura e la competenza sui social. <br><br>
È così piacevole poter leggere i pensieri dell’autrice in modo più argomentato rispetto ai post online! E inoltre 
è generosissima con i contenuti. <br><br>
Sono particolarmente affezionata a questo libro perché mi è stato di grande aiuto e voglio approfittare di questo 
articolo per spiegarti perché dovresti leggerlo. <br><br>

<h2>Cosa troviamo all’interno?</h2><br>
Si inizia già dalle prime pagine a mettere carne sul fuoco quando Fabiana Andreani scrive una sorta di risposta 
al “Ci parli di lei” (un classico dei colloqui) introducendo sé stessa. <br><br>
Il suo percorso decisamente non lineare, che racconta anche nel libro, l’ha portata da un dottorato in linguistica 
giapponese ad entrare poi nel mondo dell’orientamento al lavoro entrando a far parte della
24Ore Business School e della RCS Academy Business School. <br><br>
Già dalla sua presentazione si capisce quanto sia importante <b>dare un’impressione positiva di sé ed essere organizzati nell’argomentazione, 
aiutandosi raccontando per episodi</b>. Insomma: poche righe e già 4-5 consigli che ti indirizzano verso un modo di 
fare e un’impostazione di pensiero sicuramente vincenti e applicabili direi in tante situazioni della vita in generale. <br><br>
Ho apprezzato molto la <b>panoramica sul mondo lavorativo al giorno d’oggi</b> descritto da chi quel mondo lo conosce 
bene e sa come informarsi in merito, con tanto di grafici dettagliati sulle statistiche. <br><br>
Ho trovato inoltre tanti consigli su siti online da consultare e in generale un lavoro di grafica ottimo che
rende scorrevolissima la lettura: una su tutte la <b>sezione dedicata al curriculum piena zeppa di esempi</b>. <br><br>

