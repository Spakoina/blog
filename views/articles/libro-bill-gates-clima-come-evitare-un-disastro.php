<h2>Che cosa possiamo fare noi cittadini?</h2><br>
L’ultima parte del libro è dedicata alle cose che possiamo fare già da oggi. <br><br>
Le leggi e le norme ormai sono troppo datate, non elaborate tenendo conto del cambiamento climatico 
(Gates si riferisce esplicitamente alle leggi statunitensi, ma non credo che in Italia sia stato fatto qualcosa di diverso). 
Un cambiamento della politica
sarebbe quindi auspicabile. Cosa si può fare adesso in quanto consumatori, 
in quanto cittadini e in quanto dipendenti o datori di lavoro? <br><br>

Ecco qualche proposta:<br>
scrivere lettere con richieste ai politici,<br> 
frequentare i municipi, guardare alla propria realtà locale oltre che a quella nazionale,<br> 
provare hamburger vegetali<br><!-- comment -->
e, per le aziende, adottare carbon tax interne. <br>
Oltre all'importanza della ricerca e dello sviluppo Gates evidenzia anche il ruolo di incentivi e misure fiscali come
una tassa sull’anidride carbonica. <br><br>

Una soluzione da lui elaborata è l'adozione del cosiddetto Green Premium che spiega bene anche 
<a class="article-title-link" href="hhttps://www.gatesnotes.com/Energy/Introducing-the-Green-Premiums" target="_blank"><b>qui su un articolo nel suo blog</b></a>
che è sostanzialmente la differenza di costo tra un sistema di produzione che non inquina e uno che produce invece carbonio. <br><br>

Una parte del libro è dedicata al nucleare che al momento sembra essere la soluzione più efficiente: 
inquina pochissimo e produce tanta energia. Non mi pronuncio in merito perché non ho ancora un'opinione al riguardo
e il tema è delicatissimo. Vi invito a leggere il libro per informarvi e farvi una vostra opinione.<br><br>

           
            
            
            <h2>Conclusioni sul libro</h2>
            <br>
            Consiglio questo saggio perché è aggiornato e completo. I punti di vista sono tantissimi sui quali farsi un’idea 
e informarsi. È imperniato su piani realistici e specifici. 
<br><br>Non resta che leggere questo libro e unirci tutti per la salvaguardia del nostro ambiente, 
al di là di ogni credo politico,
con l’augurio e la speranza di spendere
<em>“Più tempo ed energie a sostenere ciò a cui siete favorevoli che a opporvi a ciò a cui siete contrari”</em>.
        
        
