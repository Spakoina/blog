<h2 id="cartoni">Cartoni animati</h2>
Non so se sto per consigliarti una cosa sciocca o no, so solo che con me funziona.<br><!-- comment -->
Quando andai in Russia, per immergermi e studiare la lingua per bene, mi accorsi ben presto che le uniche conversazioni che riuscivo a capire perfettamente, per strada o in fila, erano quelle fra adulti e bambini. <br>
<b>Il linguaggio dei più piccoli o che si usa PER i più piccoli è perfetto quando si sta imparando una lingua</b>: è semplice, ma comunque efficace. 
Quale strumento ha questo tipo di linguaggio ed è divertente da guardare allo stesso tempo? I cartoni animati! <br><br>
Le piattaforme di streaming a pagamento ne sono pieni, ma anche YouTube ha di tutto. Non solo singoli video, ho scoperto canali interi dedicati all’apprendimento della lingua per bambini.<br>
Può suonare strano, ma vi assicuro che alcuni cartoni animati sono delle opere d’arte e non sempre sono adatti solo ai bambini, ma anche ai più grandi. E non ci sono solo cartoni, ma anche film o serie
che si rivolgono principalmente a bambini.<br><br>
Cerca tu stesso su internet o fatti consigliare dalle persone che conoscerai su Tandem o sulle altre app o siti che ti ho consigliato.<br>
Io ho scoperto grazie ad una ragazza brasiliana (Anita che ringrazio!) “Reinações de Narizinho” su YouTube, tratto da un classico della letteratura d’infanzia brasiliana. È stato ottimo per il mio portoghese.
