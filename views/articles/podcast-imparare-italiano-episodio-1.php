            <h2>Testo</h2>
            Qualche domenica fa sono stata nella magnifica Villa Pisani. <br><br>
            Il palazzo si trova su una strada piena di Ville Venete, lungo il fiume Brenta. <br>
            Questo fiume fu un importante collegamento con Venezia e con la città di Padova. Gli abitanti delle ville si spostavano con una barca chiamata 
            <a href="https://www.burchiello.org/it/" target="_blank"> <b>burchiello</b> (1)<i class="fas fa-external-link-alt"></i></a>. La vita in queste zone si svolgeva sull’acqua e a Venezia è tuttora così! <br><br>
            Villa Pisani è una tipica Villa Veneta e cioè una tipologia di casa in cui abitavano le persone più ricche. Al suo interno è piena di storia e di bellezza. <br>

            Il paesaggio è bellissimo e non vedo l’ora di entrare nella Villa. .<br><br>
            Parcheggiamo l’auto, facciamo il biglietto e, dopo una chiacchierata con la <b>guardia</b> (2) all’ingresso, entriamo. <br><br>
            Mi guardo intorno e sono meravigliata dalle sale piene di affreschi, <b>marmi </b>(3), mobili antichi e <b>sfarzo</b>(4). <br>

