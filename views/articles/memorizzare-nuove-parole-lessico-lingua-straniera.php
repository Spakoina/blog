Dopo aver creato la base delle flashcard?<br><!-- comment -->
Sempre da <b>"Elementi" in alto a sinistra</b> su Canva trovete un'infinità di illustrazioni gratuite da inserire e gestire come volete sulla pagina grazie alla grafica molto
intuitiva del sito. <br><br><!-- comment -->
Potete poi stamparle e <b>laminarle</b> se le decorate a mano oppure, se fate questo lavoro con dei bambini, 
<b>incollarle su dei cartoncini e tagliarle</b> insieme. <br>
Si possono anche creare dei <b>quaderni</b> magari con parole a tema inserendo le schede nei raccoglitori che si usano 
di solito per le carte da gioco o per collezionare francobolli e creare un quadernone da ripasso.<br>
